    private void btn_calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calcularActionPerformed
        double bruto = 0,descuento,bonificacion,neto;
        if(rb_maestro.isSelected()) bruto=1800;
        if(rb_capataz.isSelected()) bruto=1350;
        if(rb_obrero.isSelected()) bruto=1080;
        if(rb_ayudante.isSelected()) bruto=850;
        bonificacion=0.15*bruto;
        descuento=0.13*bruto;
        neto=bruto+bonificacion-descuento;
        txt_bruto.setText(String.format("%.2f",bruto));
        txt_bonificacion.setText(String.format("%.2f",bonificacion));
        txt_descuento.setText(String.format("%.2f",descuento));
        txt_neto.setText(String.format("%.2f",neto));
    }//GEN-LAST:event_btn_calcularActionPerformed

